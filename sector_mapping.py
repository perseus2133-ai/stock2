def map_sector(fine_sector: str) -> str:
    """세부 업종명을 광역 업종으로 매핑"""
    if not fine_sector or str(fine_sector) == 'nan':
        return "기타"
    # 1. 정확히 매핑
    if fine_sector in SECTOR_MAP:
        return SECTOR_MAP[fine_sector]
    # 2. 부분 매칭 (SECTOR_MAP)
    for key, value in SECTOR_MAP.items():
        if key in fine_sector or fine_sector in key:
            return value
    # 3. 키워드 매칭
    for keyword, sector in KEYWORD_MAP.items():
        if keyword in fine_sector:
            return sector
    return "기타"
